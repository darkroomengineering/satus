import { colors, themes } from "../colors";
import { easings } from "../easings";
import { breakpoints, customSizes, layout, screens } from "../layout";
import { fonts, typography } from "../typography";
import { generateMedia } from "./generate-media";
import { generateRoot } from "./generate-root";
import { generateScale } from "./generate-scale";
import { generateTailwind } from "./generate-tailwind";

const tailwind = generateTailwind({ breakpoints, colors, customSizes, easings, fonts, themes, typography });
const root = generateRoot({ colors, customSizes, easings, layout, screens });
const scale = generateScale();
const media = generateMedia({ breakpoints });

const banner = `/*
 * THIS FILE IS GENERATED BY setup-styles.ts
 * DO NOT EDIT IT DIRECTLY.
 */`;

await Bun.write("./styles/css/tailwind.css", [banner, tailwind, scale].join("\n\n"));
await Bun.write("./styles/css/root.css", [banner, root].join("\n\n"));
await Bun.write("./styles/css/media.css", [banner, media].join("\n\n"));

console.log(
  Bun.color("green", "ansi"),
  "✓",
  Bun.color("black", "ansi"),
  "Style config generated successfully",
);
